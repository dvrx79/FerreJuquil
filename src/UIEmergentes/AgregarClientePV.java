    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        try{
            String nombreCliente = txtNombre.getText();
            String ApellidoPaterno = txtAPaterno.getText();
            String ApellidoMaterno = txtAMaterno.getText();
            String NumeroTelefonico = txtNumeroTel.getText();
            Pueblo puebloSeleccionado = (Pueblo) cboPueblos.getSelectedItem();
            int idPueblo = puebloSeleccionado.getId();
            int CodigoPostal = 1; //esto se va a quitar
            String calle = txtCalle.getText();
            String numero = txtNumeroCasa.getText();
            String rfc;
            if(txtRfc.getText().equals("Ingresa el RFC del cliente") || txtRfc.getText().isEmpty()){
                rfc = null;
            }else{
                rfc = txtRfc.getText();
            }
            
            
            if(nombreCliente.isEmpty() || nombreCliente.equals("Ingresa un nombre")){
                JOptionPane.showMessageDialog(null, "No puedes dejar campos vacios, Ingresa un nombre");
            }
            if(ApellidoPaterno.isEmpty() || ApellidoPaterno.equals("Ingresa el Apellido Paterno")){
                JOptionPane.showMessageDialog(null, "No puedes dejar campos vacios, Ingresa un Apellido Paterno");
            }
            if(ApellidoMaterno.isEmpty() || ApellidoMaterno.equals("Ingresa el Apellido Materno")){
                JOptionPane.showMessageDialog(null, "No puedes dejar campos vacios, Ingresa un Apellido Materno");
            }
            if(NumeroTelefonico.isEmpty() || NumeroTelefonico.equals("Ingresa un numero Telefonico")){
                JOptionPane.showMessageDialog(null, "No puedes dejar campos vacios, Ingresa un Numero Telefonico");
            }
            if(calle.isEmpty() || calle.equals("Ingresa el nombre de una calle")){
                JOptionPane.showMessageDialog(null, "No puedes dejar campos vacios, Ingresa un nombre de calle");
            }
            if(numero.isEmpty() || numero.equals("Ingresa el numero de la casa")){
                JOptionPane.showMessageDialog(null, "No puedes dejar campos vacios, Ingresa un numero de casa");
            }else{
                int idDir = manejoclientes.insertarDireccion(calle, numero, idPueblo, CodigoPostal);
                idCliente = manejoclientes.insertarCliente(rfc, nombreCliente, ApellidoPaterno, ApellidoMaterno, NumeroTelefonico, idDir);
                pagarventa.setIdCliente(idCliente);
                
                JOptionPane.showMessageDialog(null, "Cliente Agregado");
                aceptado = true;
                this.dispose();
            }
            
        }catch(SQLException e){
            
        }
      
    }//GEN-LAST:event_btnAceptarActionPerformed

